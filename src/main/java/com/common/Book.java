package com.common;


public class Book {

	private String bookName;

	public Book() {
		super();
	}

	public Book(String bookName) {
		this.bookName = bookName;
	}

	public String getBookName() {
		return bookName;
	}

	public void setBookName(String bookName) {
		this.bookName = bookName;
	}

	@Override
	public String toString() {
		return "Book [bookName=" + this.bookName + "]";
	}

}
